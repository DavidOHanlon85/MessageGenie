package messageGenie;

/**
 * 
 */
public class MessageGenie {

	/**
	 * Displays a morning greeting message.
	 */

	public void morningGreeting() {
		System.out.println("Good morning");
	}

	/**
	 * Displays an afternoon greeting message.
	 */
	public void afternoonGreeting() {
		System.out.println("Afternoon");
	}

	/**
	 * Displays an evening greeting message.
	 */
	public void eveningGreeting() {
		System.out.println("Evening time. Relax");
	}

	/**
	 * Displays a goodnight greeting message.
	 */
	public void goodnightGreeting() {
		System.out.println("Don't let the bed bugs bite");
	}

}
